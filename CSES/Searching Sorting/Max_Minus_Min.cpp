void marwan()
{
    int n;
    cin >> n;
    set<int> s;
    for (int i = 0; i < n; i++)
    {
        int x;
        cin >> x;
        s.insert(x);
    }
    int ans = *(--s.end()) - *s.begin();
    while (*(--s.end()) < 1e9)
    {
        int x = *s.begin();
        s.erase(s.begin());
        s.insert(x << 1);
        ans = min(ans, *(--s.end()) - *s.begin());
    }
    cout << ans << endl;
}
