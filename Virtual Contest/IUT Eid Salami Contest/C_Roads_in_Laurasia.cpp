void marwan() {
    int n;
    cin >> n;
    vector<vector<int>> g(n + 1);
    for (int i = 1; i < n; i++) {
        int u, v;
        cin >> u >> v;
        g[u].push_back(v);
        g[v].push_back(u);
    }
    int count = 0;
    for (int i = 1; i <= n; i++) {
        if (g[i].size() == 1)
            count++;
    }
    cout << (count / 2) + (count % 2 != 0) << endl;
}
